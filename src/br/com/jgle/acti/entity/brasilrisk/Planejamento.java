package br.com.jgle.acti.entity.brasilrisk;

public class Planejamento {

	protected String previsao_de_inicio_data;
	protected String previsao_de_inicio_hora;
	protected String previsao_de_termino_data;
	protected String previsao_de_termino_hora;
	protected String itinerario;

	public String getPrevisao_de_inicio_data() {
		return previsao_de_inicio_data;
	}

	public void setPrevisao_de_inicio_data(String previsao_de_inicio_data) {
		this.previsao_de_inicio_data = previsao_de_inicio_data;
	}

	public String getPrevisao_de_inicio_hora() {
		return previsao_de_inicio_hora;
	}

	public void setPrevisao_de_inicio_hora(String previsao_de_inicio_hora) {
		this.previsao_de_inicio_hora = previsao_de_inicio_hora;
	}

	public String getPrevisao_de_termino_data() {
		return previsao_de_termino_data;
	}

	public void setPrevisao_de_termino_data(String previsao_de_termino_data) {
		this.previsao_de_termino_data = previsao_de_termino_data;
	}

	public String getPrevisao_de_termino_hora() {
		return previsao_de_termino_hora;
	}

	public void setPrevisao_de_termino_hora(String previsao_de_termino_hora) {
		this.previsao_de_termino_hora = previsao_de_termino_hora;
	}

	public String getItinerario() {
		return itinerario;
	}

	public void setItinerario(String itinerario) {
		this.itinerario = itinerario;
	}
}
